# 1280x720 to 1920x1080 converter

original_width = int(input("enter width: \n"))
converted_width = (original_width / 1280) * 1920
print("converted width is ", converted_width)

print("\n")

original_height = int(input("enter height: \n"))
converted_height = (original_height / 720) * 1080
print("converted height is ", converted_height)
